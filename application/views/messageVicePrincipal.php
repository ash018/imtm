                <p>My heartiest welcome from Dhaka Polytechnic Institute (DPI). Its a technical vocational engineering institution located city of Bangladesh&nbsp; at the heart of the capital . This institute was established in1955 with the concept of producing product in to productivity. DPI has an excellent TVET friendly academic environment. It is reliable and dependable.DPI aims at working closely with the industries to satisfy the needs of job market home and abroad. We provide higher order skilled manpower. We provide support to the passing out TVET graduates for placement into the decent job. We are committed to carryout the mission and visions of TVET related agenda of the government. My best wishes to you all.</p>
